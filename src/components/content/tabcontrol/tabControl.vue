<template>
  <div class="tab-control">
    <div
      v-for="(item,index) in tabcontrol"
      :key="item"
      class="tab-control-item"
      :class="{active : index === currentIndex}"
      @click="itemClick(index)"
    >
      <span>{{item}}</span>
    </div>
  </div>
</template>
<script>
export default {
  name: "tabControl",
  props: {
    tabcontrol: {
      type: Array,
      default() {
        return [];
      }
    }
  },
  data() {
    return {
      currentIndex: 0
    };
  },
  methods: {
    itemClick(index) {
      this.currentIndex = index;
      this.$emit('tabClick',index)
    }
  }
};
</script>
